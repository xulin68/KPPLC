	function transition(obj,value){
		var num=obj.offsetHeight
		var nav_value=num
		var timer=setInterval(function(){
			if (num<value)	//起始值是小于终点值	展开
			{
				if (nav_value<=value) 
				{
					nav_value=nav_value+10
					obj.style.height=nav_value+'px'
				}
				else
				{
					clearInterval(timer)
					obj.style.height=value+'px'
				}
			}
			else			//起始值是大于终点值	收缩
			{
				if (nav_value>=value) 
				{
					nav_value=nav_value-10
					obj.style.height=nav_value+'px'
				}
				else
				{
					clearInterval(timer)
					obj.style.height=value+'px'
				}
			}
		},10)
	}
	function navshow(id){
		var nav=document.getElementById(id)
		var nav_Height=nav.children[0].children[0].offsetHeight+nav.children[0].children[1].offsetHeight+41	//获取nav的高度
		if (nav.offsetHeight=='42')	//检测折叠状态
		{
			transition(nav,nav_Height)

			nav.children[0].children[0].children[0].innerHTML='▼'
		}
		else
		{
			transition(nav,41)
			nav.children[0].children[0].children[0].innerHTML='▲'
		}
	}
